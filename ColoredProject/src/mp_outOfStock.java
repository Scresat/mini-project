    public void UpdateTable(){
          try {
            String sql = "select medicine.name as 'Name', medicine.expiry as 'Expiry Date', medicine.price as 'Price', medicine.type as 'Type', medicine.quantity as 'Quantity' from out_of_stock, medicine where out_of_stock.id = medicine.id;";
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            table_outofstock.setModel(DbUtils.resultSetToTableModel(rs));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        } finally {
            try {
                rs.close();
                pst.close();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }
